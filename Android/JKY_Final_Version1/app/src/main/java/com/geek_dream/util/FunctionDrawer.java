public class FunctionDrawer {

    private int id_imgFun;
    private String name_tvFun;

    public FunctionDrawer(int id_imgFun, String name_tvFun) {
        super();
        this.id_imgFun = id_imgFun;
        this.name_tvFun = name_tvFun;
    }

    public int getId_imgFun() {
        return id_imgFun;
    }

    public void setId_imgFun(int id_imgFun) {
        this.id_imgFun = id_imgFun;
    }

    public String getName_tvFun() {
        return name_tvFun;
    }

    public void setName_tvFun(String name_tvFun) {
        this.name_tvFun = name_tvFun;
    }


}
